    public int[] addNegabinary(int[] arr1, int[] arr2) {
        List<Integer> result = new ArrayList<>();
        // -2进制，位权为-2
        List<Integer> listArr1 = Arrays.stream(arr1).boxed().collect(Collectors.toList());
        List<Integer> listArr2 = Arrays.stream(arr2).boxed().collect(Collectors.toList());
        Collections.reverse(listArr1);
        Collections.reverse(listArr2);
        System.out.println(listArr1);
        System.out.println(listArr2);

        int carry = 0;
        for (int i = 0; (i < Math.max(listArr1.size(), listArr2.size())) || (carry != 0); i++) {
            int a1 = i < listArr1.size() ? listArr1.get(i) : 0;
            int a2 = i < listArr2.size() ? listArr2.get(i) : 0;

            int a3 = a1 + a2 + carry;
            carry = a3 / -2;
            a3 = a3 % -2;
            if (a3 < 0) {
                carry++;
                a3 = -a3;
            }
            result.add(a3);
        }

        while (result.size() > 1 && result.get(result.size() - 1) == 0) {
            result.remove(result.size() - 1);
        }

        Collections.reverse(result);
        int[] a = new int[result.size()];
        for (int i = 0; i < result.size(); i++) {
            a[i] = result.get(i);
        }
        return a;
    }

